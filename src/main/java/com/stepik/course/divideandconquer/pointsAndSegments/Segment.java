package com.stepik.course.divideandconquer.pointsAndSegments;

public class Segment {
    int left;
    int right;

    public int getLeft() {
        return left;
    }

    public int getRight() {
        return right;
    }

    public Segment(int left, int right) {
        this.left = left;
        this.right = right;
    }
}
